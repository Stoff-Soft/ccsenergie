    <h1>Ökogas – darauf kommt es an</h1>
<p>Ökogas wird in sogenannten Biogasanlagen erzeugt. Bereits 2009 setzte sich Deutschland mit ca. 5.000 Biogasanlagen an die europäische Spitze. 2017 waren es bereits über 9.000 Anlagen, in denen Biogas erzeugt wurde. Gewonnen wird Biogas aus regenerativen Quellen wie der Vergärung von Pflanzen, Abfall oder Dünger. Bei Gastarifen mit Biogas muss unterschieden werden, ob es sich lediglich um einen Biogas-Anteil oder 100 % Biogas handelt. Zudem gibt es Biogastarife, die beim genauen Hinsehen gar kein „echtes“ Biogas liefern. Der Verbraucher wird mit herkömmlichem Erdgas beliefert und darauf aufmerksam gemacht, dass der Gasanbieter im Gegenzug die bei der Verbrennung entstehenden CO2-Emissionen „ausgleicht“. So unterstützen einige Gasanbieter Aufforstungsprojekte, um die CO2-Emissionen auszugleichen oder kaufen Emissionszertifikate. Letztere sind aus gutem Grund in die Kritik geraten, weil sich herausstellte, dass Energiekonzerne mithilfe eingekaufter Emissionsrechte normales Erdgas einfach umetikettieren – und nicht weiter in den Ausbau erneuerbarer Energien investieren. Wir richten uns beim Ökogas ganz nach Ihren Wünschen und vermitteln günstige Ökogastarife, die dem ökologischen Gedanken auch gerecht werden.
</p>


<h2>Lassen Sie uns die Gasversorgung intelligent optimiert vorantreiben</h2>

<p>Der Bundesgerichtshof hat mit seinem Urteil die Verbraucher im Hinblick auf den Automatismus steigender Gaspreise gestärkt. Gastarife vergleichen, lohnt sich deshalb. Allerdings machen noch immer viel zu wenig Gaskunden von der Möglichkeit Gebrauch, den Gastarif zu optimieren. Das mag auch daran liegen, dass es für den Verbraucher äußerst schwer ist, den optimalen Gastarif aus der Masse der Tarife herauszufiltern. Mal eben schnell einen Gastarifvergleich durchführen und das billigste Produkt auswählen, kann sich als unklug erweisen. Anfängliche Rabatte könnten einen später teuren Tarif zunächst günstig erscheinen lassen. Zudem glänzen längst nicht alle Gasanbieter mit einer transparenten Vertragsgestaltung. Wir machen auf mögliche Tücken aufmerksam und bieten Ihnen nur seriöse Gasanbieter mit entsprechender Energie-Qualität an. Um Ihren Gastarif zu optimieren, händigen Sie uns einfach die letzte Abschlagsrechnung aus. Dort sind alle Parameter gelistet, die für eine günstige Gastarifsuche nötig sind. Selbstverständlich lassen wir in die Prognose auch mögliche betriebliche Veränderungen einfließen, die sich im anstehenden Jahr auf den Energieverbrauch auswirken könnten.
</p>
